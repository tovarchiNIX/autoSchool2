public class Task16 {

    /**
     * 16 Используя цикл for вывести на экран:
     *
     * - горизонтальную линию из 10 восьмёрок
     *
     * - вертикальную линию из 10 восьмёрок.
     */

    public static void main(String[] args) {
        for (int i = 1; i <= 10; i++) {
            System.out.print(8);
        }

        for (int i = 0; i <= 10 ; i++) {
            System.out.println(8);
        }
    }
}
